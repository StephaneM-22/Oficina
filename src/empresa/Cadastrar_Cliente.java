    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       try{
           String ende = (this.rua.getText())+ (" ") + ("N*") + (this.n.getText() + (",") +(this.bairro.getText())+ (",") + (this.cidade.getText()));
           
            Connection conexao = (Connection) Conexao.getConnection();
            String sql = "insert into cliente values (?,?,?,?);";
            
            java.sql.PreparedStatement statement = conexao.prepareStatement(sql);
            statement.setString(1,this.nome.getText());
            statement.setString(2,this.cpf.getText());
            statement.setString(3,ende);
            statement.setString(4,this.telefone.getText());
            statement.execute(); 
         
            conexao.close();
       
        }catch (SQLException ex){
            System.out.println(ex);
        }
       
       if(this.placa != null){
           try{
           
            Connection conexao = (Connection) Conexao.getConnection();
            String sql = "insert into veiculo values (?,?,?,?,?);";
            
            java.sql.PreparedStatement statement = conexao.prepareStatement(sql);
            statement.setString(1,this.cpf.getText());
            statement.setString(2,this.placa.getText());
            statement.setString(3,this.modelo.getText());
            statement.setString(4,this.cor.getText());
            statement.setInt(5,Integer.parseInt(this.km.getText()));
            statement.execute(); 
         
            conexao.close();
       
        }catch (SQLException ex){
            System.out.println(ex);
        }
        
    }

    }//GEN-LAST:event_jButton1ActionPerformed

